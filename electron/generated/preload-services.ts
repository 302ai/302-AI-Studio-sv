import { ipcRenderer } from 'electron';

// 自动生成的IPC服务接口
export interface AutoGeneratedIpcServices {
  attachments: {
    openExternal(url: string): Promise<void>;
    openExternal2(url: string): Promise<void>;
    openExternal3(url: string): Promise<void>;
    openExternal4(url: string): Promise<void>;
  };
}

// 自动生成的服务实现
export const autoGeneratedServices: AutoGeneratedIpcServices = {
  attachments: {
    openExternal: (url: string) =>
      ipcRenderer.invoke('app:attachments:openExternal', url),
    openExternal2: (url: string) =>
      ipcRenderer.invoke('app:attachments:openExternal2', url),
    openExternal3: (url: string) =>
      ipcRenderer.invoke('app:attachments:openExternal3', url),
    openExternal4: (url: string) =>
      ipcRenderer.invoke('app:attachments:openExternal4', url),
  },
};

// 导出类型声明扩展
export interface ElectronAPIExtension {
  attachments: AutoGeneratedIpcServices['attachments'];
}
